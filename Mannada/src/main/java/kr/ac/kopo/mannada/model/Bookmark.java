package kr.ac.kopo.mannada.model;

public class Bookmark {
	private int id;
	private int mannaId;
	private int num;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getMannaId() {
		return mannaId; 
	}
	public void setMannaId(int mannaId) {
		this.mannaId = mannaId;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}	
}
